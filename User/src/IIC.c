//PB8 -> IIC1_SCL; PB9 -> IIC1_SDA
void Init_IIC_Gpio(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    //RCC_AHBPeriphClockCmd(RCC_AHBPeriph_GPIOB, ENABLE );
    
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8 | GPIO_Pin_9;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT ;   //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOB, &GPIO_InitStructure);
//    IIC_SCL_0;
//    IIC_SDA_0;
}



//产生IIC起始信号
void IIC_Start(void)
{
	SDA_OUT();    //sda线输出
	IIC_SDA_1;	  	  
	IIC_SCL_1;
	delay_us(4);
 	IIC_SDA_0;  //START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC_SCL_0;  //钳住I2C总线，准备发送或接收数据 
}

//产生IIC停止信号
void IIC_Stop(void)
{
	SDA_OUT();//sda线输出
	IIC_SCL_0;
	IIC_SDA_0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC_SCL_1;
    delay_us(4);    
	IIC_SDA_1;//发送I2C总线结束信号							   	
}

//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u1 IIC_Wait_Ack(void)
{
	u1 ucErrTime = 0,x_sda = 0;
	SDA_IN();      //SDA设置为输入  
	IIC_SDA_1; delay_us(1);	   
	IIC_SCL_1; delay_us(1);
    x_sda = READ_SDA;   
	while(x_sda)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_Stop();
			return 1;
		}
        x_sda = READ_SDA;
	}
	IIC_SCL_0;//时钟输出0 	   
	return 0;  
} 

//产生ACK应答
void IIC_Ack(void)
{
	IIC_SCL_0;
	SDA_OUT();
	IIC_SDA_0;
	delay_us(2);
	IIC_SCL_1;
	delay_us(2);
	IIC_SCL_0;
}
//不产生ACK应答		    
void IIC_NAck(void)
{
	IIC_SCL_0;
	SDA_OUT();
	IIC_SDA_1;
	delay_us(2);
	IIC_SCL_1;
	delay_us(2);
	IIC_SCL_0;
}

//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_Send_Byte(u1 txd)
{                        
    u1 t;   
	SDA_OUT(); 	    
    IIC_SCL_0;//拉低时钟开始数据传输
    for(t = 0; t < 8; t++)
    {              
        //IIC_SDA=(txd&0x80)>>7;
		if((txd & 0x80) >> 7)
        {
			IIC_SDA_1;
        }
		else
        {
			IIC_SDA_0;
        }
		txd <<= 1; 	  
		delay_us(2);  
		IIC_SCL_1;
		delay_us(2); 
		IIC_SCL_0;	
		delay_us(2);
    }	 
} 	    
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u1 IIC_Read_Byte(unsigned char ack)
{
	unsigned char i,receive=0,x_sda = 0;
	SDA_IN();//SDA设置为输入
    for(i = 0; i < 8; i++ )
	{
        IIC_SCL_0; 
        delay_us(2);
		IIC_SCL_1;
        receive <<= 1;
        x_sda = READ_SDA;
        if(x_sda) receive++;   
		delay_us(1); 
    }					 
    if (!ack)
        IIC_NAck();//发送nACK
    else
        IIC_Ack(); //发送ACK   
    return receive;
}
